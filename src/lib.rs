fn word_to_nibbles([hi, lo]: &[u8; 2]) -> [u8; 4] {
    [
        (hi & 0b1111_0000) >> 4,
        hi & 0b0000_1111,
        (lo & 0b1111_0000) >> 4,
        lo & 0b0000_1111,
    ]
}
